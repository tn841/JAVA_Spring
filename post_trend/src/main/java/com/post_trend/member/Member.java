package com.post_trend.member;
/*
 *    이름             널        유형           
	-------------- -------- ------------ 
	MEMBER_NO      NOT NULL NUMBER(10)   
	MEMBER_ID               VARCHAR2(50) 
	MEMBER_PW               VARCHAR2(50) 
	MEMBER_NAME             VARCHAR2(50) 
	MEMBER_AGE              NUMBER(10)   
	MEMBER_GENDER           VARCHAR2(10) 
	MEMBER_EMAIL            VARCHAR2(50) 
	MEMBER_JOB              VARCHAR2(20) 
	MEMBER_MARRIED          NUMBER(10)   
	FAVORITE_NO             NUMBER(15)   
 */
public class Member {
	private int 	member_no;
	private String 	member_id;
	private String 	member_pw;
	private String 	member_name;
	private int 	member_age;
	private String 	member_gender;
	private String 	member_email;
	private String 	member_job;
	private int 	member_isMarried;
	private int 	member_favorite_no;
	
	
	public Member() {
		// TODO Auto-generated constructor stub
	}
	

	public Member(String member_id, String member_pw, String member_name) {
		super();
		this.member_id = member_id;
		this.member_pw = member_pw;
		this.member_name = member_name;
	}



	public Member(String member_id, String member_pw, String member_name, int member_age, String member_gender,
			String member_email, String member_job, int member_isMerried, int member_favorite_no) {
		super();
		this.member_id = member_id;
		this.member_pw = member_pw;
		this.member_name = member_name;
		this.member_age = member_age;
		this.member_gender = member_gender;
		this.member_email = member_email;
		this.member_job = member_job;
		this.member_isMarried = member_isMerried;
		this.member_favorite_no = member_favorite_no;
	}





	public int getMember_no() {
		return member_no;
	}
	public void setMember_no(int member_no) {
		this.member_no = member_no;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getMember_pw() {
		return member_pw;
	}
	public void setMember_pw(String member_pw) {
		this.member_pw = member_pw;
	}
	public String getMember_name() {
		return member_name;
	}
	public void setMember_name(String member_name) {
		this.member_name = member_name;
	}
	public int getMember_age() {
		return member_age;
	}
	public void setMember_age(int member_age) {
		this.member_age = member_age;
	}
	public String getMember_gender() {
		return member_gender;
	}
	public void setMember_gender(String member_gender) {
		this.member_gender = member_gender;
	}
	public String getMember_email() {
		return member_email;
	}
	public void setMember_email(String member_email) {
		this.member_email = member_email;
	}
	public String getMember_job() {
		return member_job;
	}
	public void setMember_job(String member_job) {
		this.member_job = member_job;
	}
	public int getMember_isMarried() {
		return member_isMarried;
	}
	public void setMember_isMarried(int member_isMarried) {
		this.member_isMarried = member_isMarried;
	}
	public int getMember_favorite_no() {
		return member_favorite_no;
	}
	public void setMember_favorite_no(int member_favorite_no) {
		this.member_favorite_no = member_favorite_no;
	}
	
	
	
}
